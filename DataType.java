public class DataType {
    public static void main(String[] args) {
        byte b = -128;//to 127 & 8 bit or  1 byte
        System.out.println(b);
        short s = -32768;//to 32768 & 16 bit or 2 byte
        System.out.println(s);
        int i = -2147483648; // to 2147483647 & 32 bit or 4 byte
        System.out.println(i);
        long l = -9223372036854775808l; //to 9223372036854775807l & 64 bit or 8 byte
        System.out.println(l);
        char ch = 'A';
        System.out.println(ch);
        float f = 29.87f;
        System.out.println(f);
        double d = 2876878968.7077;
        System.out.println(d);
        boolean b1 = true;
        System.out.println(b1);
    }
}